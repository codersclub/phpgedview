*/
class PGVRElement {
	/**
	* Raw Text
	* @var string $text
	*/
	public $text = "";

	/**
	* Element renderer
	*
	* @param &$renderer
	* @return 0
	*/
	function render(&$renderer) {
//		print "Nothing rendered.  Something bad happened";
//		debug_print_backtrace();
