		return 0;
	}

	function getHeight(&$renderer) {
		return 0;
	}

	function getWidth(&$renderer) {
		return 0;
	}

	function addText($t) {
		global $embed_fonts, $SpecialOrds, $pgvreport, $reportTitle, $reportDescription;

		if (!is_null($t)) {		// PHP 8.1.1 doesn't like NULL
			foreach($SpecialOrds as $ord) {
				if (strpos($t, chr($ord))!==false) {
					$embed_fonts = true;
				}
			}
			$t = trim($t, "\r\n\t");
			$t = str_replace(array("<br />", "&nbsp;"), array("\n", " "), $t);
			if (!PGV_RNEW) {
				$t = strip_tags($t);
				$t = unhtmlentities($t);
			}
			$this->text .= $t;
		}

		// Adding the title and description to the Document Properties
		if ($reportTitle) {
			$pgvreport->addTitle($t);
		} elseif ($reportDescription) {
			$pgvreport->addDescription($t);
		}
		return 0;
	}

	function addNewline() {
		$this->text .= "\n";
		return 0;
	}

	function getValue() {
		return $this->text;
	}

	function setWrapWidth($wrapwidth, $cellwidth) {
		return 0;
	}

	function renderFootnote(&$renderer) {
		return false;
		//-- to be implemented in inherited classes
	}

	/**
	* Get the Class name type
	*
	* @return string PGVRElementBase
	*/
	function get_type() {
		return "PGVRElementBase";
	}

	function setText($text) {
		$this->text = $text;
		return 0;
	}

} //-- END PGVRElement

/**
* HTML element class
*
* @package PhpGedView
* @subpackage Reports
*/
class PGVRHtml extends PGVRElement {
	public $tag;
	public $attrs;
	public $elements = array();

	function __construct($tag, $attrs) {
		$this->tag = $tag;
		$this->attrs = $attrs;
		return 0;
	}

	function getStart() {
		$str = "<".$this->tag." ";
		foreach($this->attrs as $key=>$value) {
			$str .= $key."=\"".$value."\" ";
		}
		$str .= ">";
		return $str;
	}

	function getEnd() {
		return "</".$this->tag.">";
	}

	function addElement($element) {
		$this->elements[] = $element;
		return 0;
	}

	/**
	* Get the Class Type
	* @return object PGVRHtml
	*/
	function get_type() {
		return "PGVRHtml";
	}
}

